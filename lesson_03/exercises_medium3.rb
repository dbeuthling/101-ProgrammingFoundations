def tricky_method_two(a_string_param, an_array_param)
  a_string_param.gsub!('pumpkins', 'rutabaga') # modifies the caller
  an_array_param = ['pumpkins', 'rutabaga'] # does not modify the caller
  p a_string_param # "rutabaga"
  p a_string_param.object_id # same as my_string
  p an_array_param.object_id # unique id
end

my_string = "pumpkins"
my_array = ["pumpkins"]
tricky_method_two(my_string, my_array)

puts "My string looks like this now: #{my_string}" # modified by method to be rutabaga
puts "My array looks like this now: #{my_array}" # unmodified by method
p my_string.object_id # same as a_string_param
p my_array.object_id # unique id

