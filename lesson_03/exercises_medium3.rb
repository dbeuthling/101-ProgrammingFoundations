def tricky_method(a_string_param, an_array_param)
  a_string_param += " rutabaga" # does not modify the caller
  an_array_param << "rutabaga" # modifies the caller
  p a_string_param # "pumpkins rutabaga"
  p a_string_param.object_id # different id than my_string
  p an_array_param.object_id # same id as my_array
end

my_string = "pumpkins" # "pumpkins"
my_array = ["pumpkins"] # ["pumpkins"]
tricky_method(my_string, my_array)

puts "My string looks like this now: #{my_string}" # Should remain pumpkins
puts "My array looks like this now: #{my_array}" # Will become ["pumpkins", "rutabaga"]
p my_string.object_id # different from a_string_param id
p my_array.object_id # same as an_array_param id
puts "---------------------"

